    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        if(tbl.getRowCount()==0) {
            JOptionPane.showMessageDialog(this, "danh sach rong");
        }
        int b = JOptionPane.showConfirmDialog(null, "do you want to delete this customer?",null,JOptionPane.YES_NO_OPTION);
        List<customer> ls = customerDAO.getAll();
        if ( b == 0){
            if (tbl.getSelectedRowCount()==1){
            customer cus = ls.get(tbl.getSelectedRow());
            int a = new customerDAO().delete(cus);
            if (a==-1){
                JOptionPane.showMessageDialog(null,"Delete unsucessful");
            }else{
                model.removeRow(tbl.getSelectedRow());
            }
        }else JOptionPane.showMessageDialog(null,"row not selected");
        }
            
    }//GEN-LAST:event_deleteActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if(tbl.getSelectedRowCount()==0){
            JOptionPane.showMessageDialog(null,"Row not selected");
        }else{
            List<customer> ls = customerDAO.getAll();
            //new gui4();
            setVisible(false);
            gui4 g = new gui4();
            g.cusName.setText(ls.get(tbl.getSelectedRow()).getName());
            g.cusEmail.setText(ls.get(tbl.getSelectedRow()).getEmail());
            g.cusAddress.setText(ls.get(tbl.getSelectedRow()).getAddress());
            g.cusPhone.setText(ls.get(tbl.getSelectedRow()).getPhone());
            g.ID.setText(String.valueOf(ls.get(tbl.getSelectedRow()).getCusID()));
            
            
        }
    }//GEN-LAST:event_updateActionPerformed

