    public int checkUser(String u, String r) {
        for (user a : userDAO.getAll()) {
            if ((a.getUsername().equals(u)) && (a.getRole().equals(r))) {
                return 1;
            }
        }
        return 0;
    }

    public int checkPass(String u, String p, String r) {
        for (user a : userDAO.getAll()) {
            if ((a.getUsername().equals(u)) && (a.getPasword().equals(p)) && (a.getRole().equals(r))) {
                return 1;
            }
        }
        return 0;
    }

