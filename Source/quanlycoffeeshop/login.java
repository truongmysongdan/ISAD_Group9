    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed

        String username = userTF.getText();
        String pass = passwordTF.getText();
        String roles = String.valueOf(role.getSelectedItem());
        if ((userTF.getText().equals("")) || (passwordTF.getText().equals(""))) {
            JOptionPane.showMessageDialog(loginbtn, "not enter enough information");
        } else {
            try {
                String sSQL = "select account,password,NAMEtype\n"
                        + "from dbo.Emloyee E join dbo.EmloyeeType ET\n"
                        + "on E.EmloyeeTypeId=et.EmloyeeTypeId " 
                        + "where account = ? and password=? and nametype= ?"
                        ;
                Connection conn = databaseHelper.getDBConnect();
                PreparedStatement sttm = conn.prepareStatement(sSQL);
                sttm.setString(1,username);
                sttm.setString(2,pass);
                sttm.setString (3,roles);
                ResultSet rs = sttm.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(loginbtn, "LOGIN SUCCESSFUL");
                    new gui2();
                    setVisible(false);
                }else {
                    JOptionPane.showMessageDialog(loginbtn, "WRONG USERNAME OR PASSWORD");
                }
            } catch (Exception e) {
                System.out.println(e);
            }

        }



    }//GEN-LAST:event_loginbtnActionPerformed

